<?php

//pre-defined array function

//max() - to print the biggest element of the array
$list = [2,4,6];

echo max($list);

echo "<br>";

//min() - to print the smallest element of the array
$list = [2,4,1,6];

echo min($list);

echo "<br>";

//print_r - to show the elements of the array
$list = [2,4,1,6];

echo print_r($list);

echo "<br>";

//sort - to short the element of the array
$list = [2,4,1,6];

echo sort($list);
print_r($list);

echo "<br>";