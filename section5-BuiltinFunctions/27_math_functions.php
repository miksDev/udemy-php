<?php

//How to use pre-defined math function

//pow function
echo pow(2,7);

echo "<br>";

//rand function - return a random number
echo rand(); //You can also include a parameter. ex. echo rand(1, 1000); 

echo "<br>";

//squareroot function
echo sqrt(100);

echo "<br>";

//ceil function - to round up numbers
echo ceil(4.9);

echo "<br>";

//floor function - to round down numbers
echo floor(4.9);

echo "<br>";

//round function - to round up
echo round(4.9);